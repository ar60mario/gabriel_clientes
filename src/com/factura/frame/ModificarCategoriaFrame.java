    private void limpiarCampos() {
        getContentPane().setBackground(new java.awt.Color(Constantes.getR(), Constantes.getG(), Constantes.getB()));
        this.setLocationRelativeTo(null);

        descripcionTxt.setText(cat.getNombreCategoria());
        codigoTxt.setEditable(false);
        codigoTxt.setText(cat.getCodigo().toString());
        descripcionTxt.requestFocus();
        if(cat.getActivo()){
            activaChk.setSelected(true);
        }else{
            activaChk.setSelected(false);
        }
    }

    private void volver() {
        AbmCategoriasFrame acf = new AbmCategoriasFrame();
        acf.setVisible(true);
        this.dispose();
    }

    private void guardar() {
        if (validar()) {
            if (activaChk.isSelected()) {
                cat.setActivo(true);
            } else {
                cat.setActivo(false);
            }
//            cat.setCodigo(cod);
            cat.setNombreCategoria(descripcionTxt.getText());
            try {
                new CategoriaService().updateCategoria(cat);
            } catch (Exception ex) {
                Logger.getLogger(ModificarCategoriaFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR");
            }
            volver();
        }
    }

    private boolean validar() {
        return true;
    }
