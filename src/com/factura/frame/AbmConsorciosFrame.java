    private void primero() {
        pagina = 0;
        consorcios = null;
            try {
                consorcios = new ConsorcioService().getAllConsorciosActivasByPagina(pagina, limite);
            } catch (Exception ex) {
                Logger.getLogger(AbmConsorciosFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (consorcios != null && !consorcios.isEmpty()) {
                llenarTabla();
                System.out.println(pagina);
            }
    }

    private void anterior() {
        String filtro = filtroTxt.getText();
        if (filtro.isEmpty()) {
            pagina -= limite;
            if (pagina < 0) {
                pagina = 0;
            }
            consorcios = null;
            try {
                consorcios = new ConsorcioService().getAllConsorciosActivasByPagina(pagina, limite);
            } catch (Exception ex) {
                Logger.getLogger(AbmConsorciosFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (consorcios != null && !consorcios.isEmpty()) {
                llenarTabla();
                System.out.println(pagina);
            }
        }
    }

    private void siguiente() {
        String filtro = filtroTxt.getText();
        Boolean hay = false;
        if (filtro.isEmpty()) {
            pagina += limite;
            consorcios = null;
            try {
                consorcios = new ConsorcioService().getAllConsorciosActivasByPagina(pagina, limite);
            } catch (Exception ex) {
                Logger.getLogger(AbmConsorciosFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (consorcios != null && !consorcios.isEmpty()) {
                llenarTabla();
                System.out.println(pagina);
            } else {
                pagina -= limite;
//                llenarTabla();
                System.out.println(pagina);
            }
        }
    }

