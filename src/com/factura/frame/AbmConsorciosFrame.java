    private void cargarLista() {
        consorcios = null;
        try {
            consorcios = new ConsorcioService().getAllConsorciosActivasByPagina(pagina, limite);
        } catch (Exception ex) {
//            Logger.getLogger(AbmConsorciosFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "TABLA VACIA");
            return;
        }
//        llenarTabla();
    }

    private void llenarTabla() {
        UtilFrame.limpiarTabla(tabla);
        if (consorcios != null && !consorcios.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Consorcio co : consorcios) {
                Object o[] = new Object[4];
                o[0] = co.getCodigo().toString();
                o[1] = co.getNombre();
                Domicilio dm = co.getDomicilio();
                String calle = dm.getCalle();
                String numero = dm.getNumero();
                o[2] = co.getCuit();
                o[3] = calle + " " + numero;
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

